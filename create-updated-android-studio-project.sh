## Troubleshooting

1. **Gradle version compatibility error**:
   - This project is configured for Gradle 8.5
   - JDK 17-20 is supported (JDK 17 recommended)

2. **Build errors**:
   - Clean and rebuild: `Build` > `Clean Project`, then `Build` > `Rebuild Project`
   - Or command line: `./gradlew clean build`

3. **JDK version issues**:
   - Ensure you're using JDK 17-20
   - Update Project Structure > SDK Location > Gradle JDK

4. **Gradle sync failed**:
   - Check internet connection for dependency downloads
   - Update Android Studio and SDK tools

## APK Installation

The generated APKs can be installed on Android devices by:
1. Enabling "Install unknown apps" in device settings
2. Transferring APK to device and opening it
3. Following installation prompts

The APKs generated by this Android Studio project use official Android build tools and proper signing, eliminating parsing errors.
