   - Click "Open an existing Android Studio project"
   - Navigate to this project folder and select it

2. **Configure JDK** (if needed):
   - Go to File > Project Structure > SDK Location
   - Set Gradle JDK to JDK 17 or 20
   - Ensure Android SDK is properly configured

3. **Sync Project**:
   - Android Studio will automatically sync Gradle files
   - Wait for the sync to complete (may take a few minutes on first run)

4. **Build APK**:
   - Go to `Build` menu > `Build Bundle(s) / APK(s)` > `Build APK(s)`
   - Or use `Build` menu > `Generate Signed Bundle / APK` for release

### Option 2: Using Command Line

1. **Set JAVA_HOME** (ensure JDK 17-20):
   ```bash
   export JAVA_HOME=/path/to/jdk-17
   ```

2. **Navigate to project directory**:
   ```bash
   cd zivora-android-studio-updated
   ```

3. **Build Debug APK**:
   ```bash
   ./gradlew assembleDebug
   ```

4. **Build Release APK**:
   ```bash
   ./gradlew assembleRelease
   ```

## APK Output Locations

- **Debug APK**: `app/build/outputs/apk/debug/app-debug.apk`
- **Release APK**: `app/build/outputs/apk/release/app-release.apk`

