# Create app icons for all densities
echo -e "${YELLOW}Creating app icons for all densities...${NC}"
for density in mdpi hdpi xhdpi xxhdpi xxxhdpi; do
    case $density in
        mdpi) size=48;;
        hdpi) size=72;;
        xhdpi) size=96;;
