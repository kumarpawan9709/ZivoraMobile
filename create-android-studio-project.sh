## Features

- **WebView-based app** loading Zivora web application
- **Proper Android project structure** with all required files
- **Multi-density app icons** (mdpi to xxxhdpi)
- **Material Design theming** with Zivora purple branding
- **Gradle build system** with both debug and release configurations
- **ProGuard configuration** for code optimization

## Troubleshooting

1. **Gradle sync failed**: 
   - Check internet connection
   - Update Android Studio and SDK tools

2. **Build errors**:
   - Clean and rebuild: `Build` > `Clean Project`, then `Build` > `Rebuild Project`
   - Or command line: `./gradlew clean build`

3. **SDK not found**:
   - Update `local.properties` with correct SDK path
   - Or let Android Studio auto-configure it

## APK Installation

The generated APKs can be installed on Android devices by:
1. Enabling "Install unknown apps" in device settings
2. Transferring APK to device and opening it
3. Following installation prompts

